# bitwise operators
n=eval(input("Enter first number:"))
m=eval(input("Enter second number"))
# & operator
And = n & m
print(f"And(&) of {n} and {m} is : {And}.")

Or = n | m
print(f"Or(|) of {n} and {m} is : {Or}.")

Xor = n ^ m
print(f"Xor(^) of {n} and {m} is : {Xor}.")

Not= ~ 1
print(f"Not(~) of {n} and {m} is : {Not}.")

Left_Shift= n << 2
print(f"Left_Shift(<<) of {n} and {m} is : {Left_Shift}.")

Right_shift= n>>2
print(f"Right_shift(^) of {n} and {m} is : {Right_shift}.")

